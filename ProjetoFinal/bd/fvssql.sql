 CREATE TABLE FichaVerificacao (
	idFichaVerificacao INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
	Local VARCHAR(45) NOT NULL,
	Empresa VARCHAR(45) NOT NULL,
	TipoLocal INTEGER NOT NULL,
	Servico VARCHAR(45) NOT NULL,
	ValorServico FLOAT NOT NULL,
	Conformidade VARCHAR(45) NOT NULL,
	Data DATE NOT NULL
	);